#include "fdf.h"

t_point	iso(float x, float y, float z, float s)
{
	t_point	p;

	x = x * s;
	y = y * s;
	p.x = (x - y) * cos(0.523599);
	p.y = (-z) + (x + y) * sin(0.523599);
	return (p);
}
